// Helper function to calculate hybrid vigor effect
export const calculateHybridVigor = (trait1, trait2) => {
  const baseValue = (trait1 + trait2) / 2;
  const heterosisBonus = 0.1; // 10% hybrid vigor
  return baseValue * (1 + heterosisBonus);
};
